package com.example.irg0.helpers;

import java.util.Dictionary;

public class Person {

    private int id;
    private String name;
    //private Date birthday
    private String info;

    public Person(int id, String name, String info) {
        this.id = id;
        this.name = name;
        this.info = info;
    }

    public int getId() {
        return id;
    }

    public String getName() {
        return name;
    }

    public String getInfo() {
        return info;
    }

    public void setId(int id) {
        this.id = id;
    }

    public void setName(String name) {
        this.name = name;
    }

    public void setInfo(String info) {
        this.info = info;
    }



}
